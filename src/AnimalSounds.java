public class AnimalSounds {
    public void AnimalSounds(String s) {
        if (s.equals("dog"))
            System.out.println("Dog says Woof Woof.");

        if(s.equals("cat"))
            System.out.println("Cat says Meow Meow.");

        if(s.equals("cow"))
            System.out.println("Cow says Moo Moo.");
    }
}
